extern "C" void app_main()
{
    Display::Master master(Const::ClockPin, Const::DataPin);
    int x = Display::MasterConst::Dimensions::Pixels::Width / 2 - DvdLogo[0].size() / 2 + Utility::RandomNumber(-46, 46);
    int y = Display::MasterConst::Dimensions::Pixels::Height / 2 - DvdLogo.size() / 2 + Utility::RandomNumber(-7, 22);
    bool goingRight = static_cast<bool>(Utility::RandomNumber(0, 1)), goingDown = static_cast<bool>(Utility::RandomNumber(0, 1));
    int normalCollisions = 0, cornerCollisions = 0;
    while (true)
    {
        bool previouslyGoingRight = goingRight, previouslyGoingDown = goingDown;
        if (!goingRight && x <= 0)
            goingRight = true;
        else if (goingRight && x + DvdLogo[0].size() >= Display::MasterConst::Dimensions::Pixels::Width)
            goingRight = false;
        if (!goingDown && y <= 16)
            goingDown = true;
        else if (goingDown && y + DvdLogo.size() >= Display::MasterConst::Dimensions::Pixels::Height)
            goingDown = false;

        if (cornerCollisions < 999 && previouslyGoingRight != goingRight && previouslyGoingDown != goingDown)
            ++cornerCollisions;
        else if (normalCollisions < 999 && (previouslyGoingRight != goingRight || previouslyGoingDown != goingDown))
            ++normalCollisions;

        char text[256];
        sprintf(text, "Results: %03d/%03d", normalCollisions, cornerCollisions);

        x += (goingRight ? 1 : -1);
        y += (goingDown ? 1 : -1);
        master.clear();
        master.print(0, 0, text);
        master.draw(x, y, DvdLogo);
        master.render();
    }
}
